    <h2 class="h2">GENERAL DISCIPLINE</h2>
    <ul>
        <li>Students must carry his/her almanac to school every day. It must have his/her photograph and identification information complete and signed by parents.</li>
        <li>Collection of funds through the students is strictly prohibited. Parents are requested not to send any contribution to the school on the basis of verbal messages from the school staff or unless they receive a written note duly signed by the Principal.</li>
    
    <li>No students should damage any school furniture, write or draw anything on walls, furniture or in any way damage things that belong to others. Any damage done will be made good by the one who causes it</li>
    <li>It is expected that juniors be treated with love and affection by the seniors while juniors show respect to their seniors.</li>
<li>No rowdy and rude behaviors are allowed by the students.</li>
<li>The school reserves the right to suspend or take strict disciplinary actions against a student whose conduct is harmful to other students or to the school.</li>
<li>Students must not miss any teaching /games/library/activity periods.</li>
<li>The observance of rules of discipline of the school and good behavior is an essential condition to a studentâ€™s continuance in the school. In case students violate the school rules or indulge in any form of in discipline , strict action like expulsion/suspension/restriction shall be taken against the student.</li>
</ul>
<h2 class="h2">SCHOOL UNIFORM</h2>
<ul>
    <li>Identity card is a part of the school uniform. </li>
    <li>Students should be neatly dressed. The school uniform is to be worn on all working days.</li>
<li>No low waist (skirts/trousers) are permitted. Shirts should be properly tucked in.</li>
<li>Boys should get their hair cut at regular intervals. Girls should plait their hair if it below their shoulders. Short hair should be neatly cut and pinned</li>
<li>Students are not to wear any jewellery to school (e.g. chains, rings, bangles,, bracelets etc) Girls may wear only one pair of ear rings or studs. The school is not responsible for goods lost.</li>
<li>In case the students are carrying any valuable articles (expensive watches, pens, cameras etc) , they will be confiscated and not returned.</li>
<li>The students should come to school in proper uniform prescribed for all the students from Std I to XII. Students without proper uniform are not allowed to attend school.</li>

</ul>
