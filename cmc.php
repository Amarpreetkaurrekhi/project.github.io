<p>DAV College Managing Committee (DAVCMC) is the largest non-governmental organisation in the field of education in India today. It has a glorious past steeped in the history of India, our great nation. DAV College Managing Committee has contributed to nation-building by pioneering private initiative in education. In our more than 135 years long career span, DAV students have emerged as leaders in their chosen fields as Nobel laureates, Prime Ministers, Scientists, Sports persons, Defense personnel,  Actors, Media persons, CEOs, etc.,  thus, making meaningful contributions to the economic and social development of India.

    DAVCMC runs and manages over  900 educational institutions comprising of public schools, grants-in-aid schools, colleges, institutes of professional education and research institutions. Trusted by both, Public as well as Private sectors, DAVCMC manages over hundred Project and Semi-project Schools for blue-chip corporate houses, such as ACC, Tata, Reliance, Adani and Public Sector Corporations such as BHEL, GAIL, SECL etc. DAVCMC discharges its social responsibility by running ashrams and orphanages for the underprivileged and destitute children and women. 
    
    The  organisation is managed by a galaxy of luminaries from diverse fields who are committed to the philosophy of Arya Samaj and ideals of Swami Dayanand Saraswati. The DAV institutions are spread all over the length and breadth of India with DAV presence in almost all states catering to a wide range of clients from the urban elite to the rural Indian. DAV's commitment to quality education ensures highly qualified and skilled work force and state-of-art infrastructure in the educational institutions managed by it.</p>
